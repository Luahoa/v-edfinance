#!/bin/bash
# beads-apply-recommendations.sh
# Automated fixes based on bv --robot-suggest and --robot-priority

set -e

PROJECT_ROOT="${PROJECT_ROOT:-$(pwd)}"
DRY_RUN="${DRY_RUN:-false}"  # Set to "true" to preview changes

echo "üîß Applying automated recommendations..."
echo ""

if [ "$DRY_RUN" = "true" ]; then
  echo "üîç DRY RUN MODE (no changes will be made)"
  echo ""
fi

cd "$PROJECT_ROOT"

if ! command -v bv &> /dev/null; then
  echo "‚ùå beads_viewer not installed. Cannot apply recommendations."
  exit 1
fi

# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
# STEP 1: Get Suggestions
# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

echo "[1/3] Fetching suggestions..."

SUGGESTIONS=$(bv --robot-suggest --json 2>/dev/null)
PRIORITY_RECS=$(bv --robot-priority --json 2>/dev/null)

if [ -z "$SUGGESTIONS" ]; then
  echo "‚ö†Ô∏è No suggestions available"
  SUGGESTIONS='{}'
fi

if [ -z "$PRIORITY_RECS" ]; then
  echo "‚ö†Ô∏è No priority recommendations available"
  PRIORITY_RECS='{}'
fi

# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
# STEP 2: Apply Dependency Fixes
# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

echo "[2/3] Applying dependency fixes..."
echo ""

# Add missing dependencies
MISSING_DEPS=$(echo "$SUGGESTIONS" | jq -r '
  (.suggestions.suggestions // []) 
  | map(select(.type == "missing_dependency"))
  | .[]
  | "\(.from_id) \(.to_id)"
' 2>/dev/null)

if [ -n "$MISSING_DEPS" ]; then
  echo "üîó Adding missing dependencies:"
  
  while IFS=' ' read -r FROM_ID TO_ID; do
    if [ -z "$FROM_ID" ] || [ -z "$TO_ID" ]; then continue; fi
    
    echo "  ‚Üí $FROM_ID blocks $TO_ID"
    
    if [ "$DRY_RUN" = "false" ]; then
      beads.exe dep add "$FROM_ID" "$TO_ID" 2>/dev/null || echo "    ‚ö†Ô∏è Failed to add dependency"
    fi
  done <<< "$MISSING_DEPS"
  echo ""
fi

# Break cycles
CYCLES=$(echo "$SUGGESTIONS" | jq -r '
  (.suggestions.suggestions // []) 
  | map(select(.type == "cycle"))
  | .[]
  | "\(.from_id) \(.to_id)"
' 2>/dev/null)

if [ -n "$CYCLES" ]; then
  echo "üîÑ Breaking cycles:"
  
  while IFS=' ' read -r FROM_ID TO_ID; do
    if [ -z "$FROM_ID" ] || [ -z "$TO_ID" ]; then continue; fi
    
    echo "  ‚Üí Remove: $FROM_ID blocks $TO_ID"
    
    if [ "$DRY_RUN" = "false" ]; then
      beads.exe dep remove "$FROM_ID" "$TO_ID" 2>/dev/null || echo "    ‚ö†Ô∏è Failed to remove dependency"
    fi
  done <<< "$CYCLES"
  echo ""
fi

# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
# STEP 3: Apply Priority Adjustments
# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

echo "[3/3] Applying priority adjustments..."
echo ""

# Only apply high-confidence recommendations (>0.7)
PRIORITY_UPDATES=$(echo "$PRIORITY_RECS" | jq -r '
  (.recommendations // []) 
  | map(select(.confidence > 0.7))
  | .[]
  | "\(.issue_id) \(.suggested_priority)"
' 2>/dev/null)

if [ -n "$PRIORITY_UPDATES" ]; then
  echo "‚öñÔ∏è Updating priorities (high-confidence only):"
  
  COUNT=0
  while IFS=' ' read -r ISSUE_ID NEW_PRIORITY; do
    if [ -z "$ISSUE_ID" ] || [ -z "$NEW_PRIORITY" ]; then continue; fi
    
    echo "  ‚Üí $ISSUE_ID: priority=$NEW_PRIORITY"
    
    if [ "$DRY_RUN" = "false" ]; then
      beads.exe update "$ISSUE_ID" --priority "$NEW_PRIORITY" 2>/dev/null || echo "    ‚ö†Ô∏è Failed to update priority"
    fi
    
    COUNT=$((COUNT + 1))
  done <<< "$PRIORITY_UPDATES"
  
  echo ""
  echo "Updated $COUNT task priorities"
else
  echo "‚úÖ No priority adjustments needed (or all low-confidence)"
fi
echo ""

# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
# STEP 4: Summary
# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

echo "‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê"
echo "RECOMMENDATIONS APPLIED"
echo "‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê"

if [ "$DRY_RUN" = "true" ]; then
  echo "üîç DRY RUN COMPLETE (no actual changes made)"
  echo ""
  echo "To apply changes, run:"
  echo "  DRY_RUN=false ./scripts/beads-apply-recommendations.sh"
else
  echo "‚úÖ Changes applied successfully"
  echo ""
  echo "Run 'bd sync' to commit changes to git"
  echo "Run './scripts/beads-graph-audit.sh' to verify"
fi

echo "‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê"

exit 0
